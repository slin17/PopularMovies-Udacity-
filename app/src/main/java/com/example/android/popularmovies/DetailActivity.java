    /**
     * A placeholder fragment containing a simple view.
     */
    public static class PlaceholderFragment extends Fragment {

        public PlaceholderFragment() {
        }

        @Override
        public View onCreateView(LayoutInflater inflater, ViewGroup container,
                                 Bundle savedInstanceState) {

            View rootView = inflater.inflate(R.layout.fragment_detail, container, false);
            Intent intent = getActivity().getIntent();
            if (intent != null && intent.hasExtra(Intent.EXTRA_TEXT)){
                String[] movieInfo = intent.getStringArrayExtra(Intent.EXTRA_TEXT);
                ((TextView) rootView.findViewById(R.id.detail_title)).setText(movieInfo[0]);
                ((TextView) rootView.findViewById(R.id.detail_overview)).setText(movieInfo[2]);
                ((TextView) rootView.findViewById(R.id.detail_rating)).setText(movieInfo[3]+"/10");
                ((TextView) rootView.findViewById(R.id.detail_date)).setText(movieInfo[4]);
                ImageView imgV = (ImageView) rootView.findViewById(R.id.detail_image);
                Picasso.with(getActivity())
                        .load(movieInfo[1])
                        .placeholder(R.color.loadingColor)
                        .into(imgV);
            }

            return rootView;
        }
    }
